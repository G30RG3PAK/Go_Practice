package main
import "fmt"
func main()  {
	var a = "Shibano" // var can declare one or more variables
	fmt.Println(a)

	var b,c int = 2019,123456 // var can declare one or more variables
	fmt.Println(b, c)

	var d = false
	fmt.Println(d)

	var n int  //declare without initialization is zero-valued
	fmt.Println(n)

	i := "Thanos" // := is shorthand for declaring and initializing a variable
	fmt.Println(i)
	
	
}